# 12345
print(bin(12345))           # 10进制转2进制
print(oct(12345))           # 10进制转8进制
print(hex(12345))           # 10进制转16进制

# 0xF98A
print(bin(0xF98A))          # 16进制转2进账
print(oct(0xF98A))          # 16进制转8进制
print(int('F98A', 16))      # 16进制换10进制


# 0b1100010110
print(oct(0b1100010110))    # 2进制转8进制
print(int('1100010110', 2)) # 2进账转10进制
print(hex(0b1100010110))    # 2进制转16进制

