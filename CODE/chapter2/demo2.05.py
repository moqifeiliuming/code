print(0b110011)

# 八进制：0123
print(0o123)

# 十六进制：0xF15
print(0xF15)

# 十进制转二进制
print(bin(12))

# 二进制转十进制
print(int("10110",2))

# 十六进转十进制
print(int("0xF35AE",16))

# 十进制转十六进制
print(hex(54321))

# 十六进制转二进制
print(bin(0xF012E))

# 二进制转十六进制
print(hex(0b1101101))

# 十进制转八进制
print(oct(1234))

